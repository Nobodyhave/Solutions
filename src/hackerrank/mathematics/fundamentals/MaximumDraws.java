import java.util.Scanner;

public class MaximumDraws {

    public static void main(String[] args) {
        final Scanner in = new Scanner(System.in);

        final int T = in.nextInt();
        for (int t = 0; t < T; t++) {
            final int pairs = in.nextInt();
            System.out.println(pairs + 1);
        }
    }
}
